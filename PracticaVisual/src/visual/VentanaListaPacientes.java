    private void lstPacientesValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_lstPacientesValueChanged
        System.out.println("Valor cambiado en la lista de pacientes a " + lstPacientes.getSelectedValue());
        switch(lstPacientes.getSelectedValue()){
            case "Jorge":
                paciente2 = Jorge;
                break;
            case "Pedro":
                paciente2 = Pedro;
                break;
            case "Alma":
                paciente2 = Alma;
                break;
            case "Luis":
                paciente2 = Luis;
                break;
        }
        LlenadoDeDatos(paciente2);
    }//GEN-LAST:event_lstPacientesValueChanged

